fun main(){

    for (a in 1..5)
        print(a)

    println()

    for(b in 1..<5)
        print(b)

    println()

    for(b in 1 until 5)
        print(b)

    println()

    for (c in 1..5 step 2)
        print(c)

    println()

    for (d in 5 downTo 1)
        print(d)

    println()

    for (e in 'a'..'d')
        print(e)

    println()

    for (f in 'z' downTo 's' step 2)
        print(f)
}